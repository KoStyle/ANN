package ann.elements;

public class ExtInput {
	private double value;

	public ExtInput() {
		super();
	}

	public ExtInput(double value) {
		super();
		this.value = value;
	}

	/**
	 * @return the value
	 */
	public double getValue() {
		return value;
	}

	/**
	 * @param value the value to set
	 */
	public void setValue(double value) {
		this.value = value;
	}
}
